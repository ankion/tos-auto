  def parse_normal_skill(data)
    @normal_skill = []
    data.each do |d|
      skill_data = d.split('|')
      skill = {
        'skillId' => skill_data[0],
        'minCoolDown' => skill_data[1],
        'maxCoolDown' => skill_data[2],
        'maxLevel' => skill_data[3]
      }
      @normal_skill << skill
    end
  end

  def parse_data(data)
    data.each do |d|
      monster = d.split('|')
      mob = @data[monster[0].to_i]
      mob['monsterId'] = monster[0]
      mob['seriesId'] = monster[1]
      mob['levelType'] = monster[2]
      mob['type'] = monster[3].to_i
